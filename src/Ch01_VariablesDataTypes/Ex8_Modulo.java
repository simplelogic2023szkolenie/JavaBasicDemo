package Ch01_VariablesDataTypes;

public class Ex8_Modulo {
    public static void main(String[] args) {
        // 10 / 3 = 3 i 1 reszty

        int a = 10;
        int b = 3;
        System.out.println(a / b);

        // % -> modulo -> reszta z dzielenia
        int result = a % b;
        System.out.println(result);
    }
}
